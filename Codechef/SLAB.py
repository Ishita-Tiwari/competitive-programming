t = int(input())
for T in range(t):
    addn = 0
    n = int(input())
    
    if n < 250000:
        addn=0
    elif n <= 500000:
        addn = ((n - 250000) * 5) // 100
    elif n <= 750000:
        addn = 12500 + ((n - 500000) * 10) // 100
    elif n <= 1000000:
        addn = 12500 + 25000 + ((n - 750000) * 15) // 100
    elif n <= 1250000:
        addn = 12500 + 25000 + 37500 + ((n - 1000000) * 20) // 100
    elif n <= 1500000:
        addn = 12500 + 25000 + 37500 + 50000 + ((n - 1250000) * 25) // 100
    else:
        addn = 12500 + 25000 + 37500 + 50000 + 62500 + ((n - 1500000) * 30) // 100
        
    print(n - addn)
